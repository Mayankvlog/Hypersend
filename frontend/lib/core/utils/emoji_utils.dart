  /// Get emoji by category name
  static List<String> getEmojisByCategory(String categoryName) {
    try {
      return categories.firstWhere((cat) => cat.name == categoryName).emojis;
    } catch (e) {
      return categories.first.emojis;
    }
  }

  /// Get all emojis (for backward compatibility)
  static List<String> getEmojiList() {
    return categories.expand((cat) => cat.emojis).toList();
  }

  /// Search emojis by name pattern
  static List<String> searchEmojis(String query) {
    if (query.isEmpty) return [];
    
    final categoryMap = {
      'smile': 0, 'smiley': 0, 'happy': 0, 'face': 0,
      'animal': 1, 'nature': 1, 'dog': 1, 'cat': 1,
      'food': 2, 'drink': 2, 'pizza': 2, 'eat': 2,
      'sport': 3, 'activity': 3, 'ball': 3, 'game': 3,
      'travel': 4, 'place': 4, 'map': 4, 'airplane': 4,
      'object': 5, 'lamp': 5, 'computer': 5, 'phone': 5,
      'symbol': 6, 'heart': 6, 'love': 6, 'check': 6,
      'flag': 7, 'country': 7,
    };
    
    for (var key in categoryMap.keys) {
      if (query.toLowerCase().contains(key)) {
        return categories[categoryMap[key]!].emojis;
      }
    }
    
    return [];
  }
